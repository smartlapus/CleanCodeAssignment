
public class MyFrameData {
	public String[] soort;
	public String[] waarde;
	public String s1;
	public String s2;
	public String w1;
	public String w2;

	public MyFrameData(String[] soort, String[] waarde, String s1, String s2,
			String w1, String w2) {
		this.soort = soort;
		this.waarde = waarde;
		this.s1 = s1;
		this.s2 = s2;
		this.w1 = w1;
		this.w2 = w2;
	}
}